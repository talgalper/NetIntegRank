read_edge_list_auto <- function(path) {
  if (!file.exists(path)) stop(sprintf("PPI file does not exist: %s", path), call. = FALSE)

  # Find first non-empty, non-comment line
  con <- file(path, open = "r")
  on.exit(close(con), add = TRUE)
  first <- ""
  while (length(first) == 0 || !nzchar(trimws(first)) || startsWith(trimws(first), "#")) {
    first <- readLines(con, n = 1, warn = FALSE)
    if (length(first) == 0) break
  }
  if (length(first) == 0) stop(sprintf("PPI file appears empty: %s", path), call. = FALSE)

  # detect delimiter and header
  delim <- if (grepl(",", first, fixed = TRUE) && !grepl("\t", first, fixed = TRUE)) "," else "\t"
  toks <- strsplit(first, delim, fixed = TRUE)[[1]]
  toks <- trimws(toks)
  header_tokens <- tolower(toks[1:min(2, length(toks))])

  header <- any(header_tokens %in% c("node1","node2","node","gene","gene1","gene2","protein1","protein2","source","target","from","to","a","b"))

  # read full table
  df <- if (delim == ",") {
    read.csv(path, header = header, stringsAsFactors = FALSE, check.names = FALSE, comment.char = "")
  } else {
    read.table(path, header = header, sep = "\t", stringsAsFactors = FALSE, check.names = FALSE, quote = "", comment.char = "")
  }

  if (ncol(df) < 2) stop("PPI edge list must have at least 2 columns.", call. = FALSE)

  extra_cols <- ncol(df) - 2
  if (extra_cols > 0) {
    message(sprintf("PPI: detected %d extra column(s) beyond columns 1-2. These will be dropped.", extra_cols))
  }

  df2 <- df[, 1:2, drop = FALSE]
  colnames(df2) <- c("a", "b")
  df2$a <- trimws(as.character(df2$a))
  df2$b <- trimws(as.character(df2$b))
  df2 <- df2[nzchar(df2$a) & nzchar(df2$b), , drop = FALSE]

  # remove self loops
  self_loops <- sum(df2$a == df2$b)
  if (self_loops > 0) message(sprintf("PPI: detected %d self-loop edge(s) (A A). These will be removed.", self_loops))
  df2 <- df2[df2$a != df2$b, , drop = FALSE]

  # remove AB/BA duplicates (undirected)
  # canonicalise
  x <- ifelse(df2$a <= df2$b, df2$a, df2$b)
  y <- ifelse(df2$a <= df2$b, df2$b, df2$a)
  key <- paste(x, y, sep = "\t")

  dup_undirected <- sum(duplicated(key))
  if (dup_undirected > 0) {
    message(sprintf("PPI: detected %d duplicate undirected edge(s) (including AB/BA). These will be removed.", dup_undirected))
  }

  df3 <- data.frame(a = x, b = y, stringsAsFactors = FALSE)
  df3 <- df3[!duplicated(paste(df3$a, df3$b, sep = "\t")), , drop = FALSE]

  message(sprintf("PPI: retained %d unique undirected edges.", nrow(df3)))
  df3
}

parse_hhnet_clusters <- function(path) {
  if (!file.exists(path)) stop(sprintf("Clusters file does not exist: %s", path), call. = FALSE)
  lines <- readLines(path, warn = FALSE)
  if (length(lines) == 0) stop(sprintf("Clusters file is empty: %s", path), call. = FALSE)

  data_lines <- lines[!grepl("^#", lines)]
  data_lines <- trimws(data_lines)
  data_lines <- data_lines[nzchar(data_lines)]

  if (length(data_lines) == 0) stop("No cluster lines found (non-comment lines).", call. = FALSE)

  clusters <- lapply(data_lines, function(x) {
    parts <- strsplit(x, "\t", fixed = TRUE)[[1]]
    parts <- trimws(parts)
    parts <- parts[nzchar(parts)]
    unique(parts)
  })
  clusters <- clusters[vapply(clusters, length, integer(1)) > 0]

  sizes <- vapply(clusters, length, integer(1))
  ord <- order(sizes, decreasing = TRUE)
  clusters <- clusters[ord]
  sizes <- sizes[ord]

  list(clusters = clusters, sizes = sizes)
}

