scale_sizes <- function(x, min_size = 1, max_size = 7) {
  if (length(x) == 0) return(numeric(0))
  if (all(is.na(x))) return(rep(min_size, length(x)))
  rng <- range(x, na.rm = TRUE)
  if (rng[1] == rng[2]) return(rep((min_size + max_size) / 2, length(x)))
  min_size + (x - rng[1]) * (max_size - min_size) / (rng[2] - rng[1])
}

plot_graph_safe <- function(g, file_prefix, seed = 1234, max_nodes = 5000, vertex_size = NULL) {
  n <- vcount(g)
  if (n > max_nodes) {
    message(sprintf("Plotting skipped for %s (n=%d > plot_max_nodes=%d). GraphML and metrics will still be written.",
                    file_prefix, n, max_nodes))
    return(invisible(NULL))
  }

  set.seed(seed)
  lay <- layout_with_fr(g)

  # PNG
  png(paste0(file_prefix, ".png"), width = 2000, height = 1500, res = 200)
  plot(g,
       layout = lay,
       vertex.label = NA,
       vertex.size = if (is.null(vertex_size)) 2 else vertex_size,
       vertex.color = vertex_attr(g, "color"),
       edge.arrow.size = 0.2,
       edge.curved = FALSE)
  dev.off()

  # PDF
  pdf(paste0(file_prefix, ".pdf"), width = 10, height = 7.5)
  plot(g,
       layout = lay,
       vertex.label = NA,
       vertex.size = if (is.null(vertex_size)) 2 else vertex_size,
       vertex.color = vertex_attr(g, "color"),
       edge.arrow.size = 0.2,
       edge.curved = FALSE)
  dev.off()
}

metrics_table <- function(g, node_id_type, cache_path) {
  ids <- vertex_attr(g, "name")
  cl <- vertex_attr(g, "cluster_id")

  deg <- degree(g, mode = "all")
  bet <- betweenness(g, directed = FALSE, normalized = TRUE)
  clo <- closeness(g, normalized = TRUE)
  eig <- eigen_centrality(g, directed = FALSE, scale = TRUE)$vector
  pr  <- page_rank(g, directed = FALSE)$vector

  df <- data.frame(
    stringsAsFactors = FALSE,
    node = ids,
    degree = as.numeric(deg),
    betweenness = as.numeric(bet),
    closeness = as.numeric(clo),
    eigen_centrality = as.numeric(eig),
    page_rank = as.numeric(pr),
    cluster = as.integer(cl)
  )
  df$source <- ifelse(is.na(df$cluster), "STRING", "subnet")

  # ID annotation to required columns
  if (node_id_type == "ensembl_gene_id") {
    df$ensembl_gene_id <- df$node
    df <- id_annot(df, input_type = "ensembl_gene_id",
                   convert_to = c("external_gene_name", "description", "gene_biotype"),
                   cache_path = cache_path)
  } else {
    # node is external_gene_name, map to ensembl + description/biotype
    df$external_gene_name <- df$node
    df <- id_annot(df, input_type = "external_gene_name",
                   convert_to = c("ensembl_gene_id", "description", "gene_biotype"),
                   cache_path = cache_path)
  }

  # Ensure required columns exist
  if (!"description" %in% colnames(df)) df$description <- NA_character_
  if (!"gene_biotype" %in% colnames(df)) df$gene_biotype <- NA_character_
  if (!"external_gene_name" %in% colnames(df)) df$external_gene_name <- NA_character_
  if (!"ensembl_gene_id" %in% colnames(df)) df$ensembl_gene_id <- NA_character_

  df$description <- ifelse(is.na(df$description), NA_character_, trimws(df$description))

  # When node_id_type is external_gene_name, ensembl_gene_id is a best-effort
  # annotation from biomaRt - NAs are acceptable and should not block the pipeline.
  # Log a warning so the user knows which genes could not be mapped.
  if (node_id_type == "external_gene_name") {
    missing_ensg <- is.na(df$ensembl_gene_id) | trimws(as.character(df$ensembl_gene_id)) == ""
    if (any(missing_ensg)) {
      message(sprintf(
        "NOTE: %d gene(s) could not be mapped to an Ensembl ID and will have NA in ensembl_gene_id: %s",
        sum(missing_ensg),
        paste(df$external_gene_name[missing_ensg], collapse = ", ")
      ))
    }
  }

  out <- df[, c("ensembl_gene_id","external_gene_name","description","gene_biotype",
               "degree","betweenness","closeness","eigen_centrality","page_rank",
               "cluster","source")]
  out
}

# -------------------------
# Main
# -------------------------

hh <- parse_hhnet_clusters(args$hhnet_clusters)
clusters_all <- hh$clusters
sizes_all <- hh$sizes

keep_idx <- which(sizes_all >= args$min_cluster_size)
if (length(keep_idx) == 0) {
  stop(sprintf("No clusters with size >= %d were found.", args$min_cluster_size), call. = FALSE)
}

clusters_filt <- clusters_all[keep_idx]
sizes_filt <- sizes_all[keep_idx]

if (args$n_clusters > 0) {
  n <- min(args$n_clusters, length(clusters_filt))
  clusters_sel <- clusters_filt[seq_len(n)]
  sizes_sel <- sizes_filt[seq_len(n)]
} else {
  clusters_sel <- clusters_filt
  sizes_sel <- sizes_filt
}

k <- length(clusters_sel)
message(sprintf("HHNet: selected %d cluster(s) (min_cluster_size=%d, n_clusters=%d).",
                k, args$min_cluster_size, args$n_clusters))

# cluster map
extra_cols <- parse_extra_colours(args$extra_colours)
cluster_cols <- make_cluster_colours(k, extra_cols)

cluster_map <- data.frame(
  cluster = seq_len(k),
  size = sizes_sel,
  colour = cluster_cols,
  stringsAsFactors = FALSE
)
write.table(cluster_map, file = file.path(args$outdir, "cluster_map.tsv"),
            sep = "\t", row.names = FALSE, quote = FALSE)

# gene -> cluster id (first assignment wins)
gene_to_cluster <- list()
dupe_genes <- character(0)
for (i in seq_len(k)) {
  for (g in clusters_sel[[i]]) {
    if (!is.null(gene_to_cluster[[g]])) dupe_genes <- c(dupe_genes, g)
    if (is.null(gene_to_cluster[[g]])) gene_to_cluster[[g]] <- i
  }
}
dupe_genes <- unique(dupe_genes)
if (length(dupe_genes) > 0) {
  message(sprintf("WARNING: %d gene(s) appeared in multiple selected clusters. They were assigned to the first cluster encountered.",
                  length(dupe_genes)))
}

cluster_genes <- unique(unlist(clusters_sel))
message(sprintf("HHNet: %d unique gene(s) across selected clusters.", length(cluster_genes)))

# Load and build full PPI graph
ppi_df <- read_edge_list_auto(args$ppi)
g_ppi <- graph_from_data_frame(ppi_df, directed = FALSE)

# Subnet induced by cluster genes (present in graph)
present <- intersect(cluster_genes, V(g_ppi)$name)
missing_in_ppi <- setdiff(cluster_genes, present)
if (length(missing_in_ppi) > 0) {
  message(sprintf("NOTE: %d cluster gene(s) were not present in the PPI graph and will be ignored in subnet construction.",
                  length(missing_in_ppi)))
}
g_sub <- induced_subgraph(g_ppi, vids = present)

# Neighbour enriched: + 1-hop neighbours of present cluster genes
neigh <- neighborhood(g_ppi, order = 1, nodes = present)
neigh_ids <- unique(unlist(lapply(neigh, function(vs) V(g_ppi)[vs]$name)))
g_neigh <- induced_subgraph(g_ppi, vids = neigh_ids)

# Assign vertex attributes: cluster_id + color
assign_cluster_attrs <- function(g) {
  ids <- V(g)$name
  cid <- vapply(ids, function(x) {
    v <- gene_to_cluster[[x]]
    if (is.null(v)) NA_integer_ else as.integer(v)
  }, integer(1))
  V(g)$cluster_id <- cid
  V(g)$color <- ifelse(is.na(cid), "white", cluster_cols[cid])
  g
}

g_sub <- assign_cluster_attrs(g_sub)
g_neigh <- assign_cluster_attrs(g_neigh)

# Save GraphML
write_graph(g_sub, file.path(args$outdir, "hhnet_subnet.graphml"), format = "graphml")
write_graph(g_neigh, file.path(args$outdir, "hhnet_subnet_neighbours.graphml"), format = "graphml")

# Plots
plot_graph_safe(g_sub, file.path(args$outdir, "hhnet_subnet"), seed = args$seed, max_nodes = args$plot_max_nodes)

deg_neigh <- degree(g_neigh)
vsize_neigh <- scale_sizes(deg_neigh, min_size = 1, max_size = 7)
plot_graph_safe(g_neigh, file.path(args$outdir, "hhnet_subnet_neighbours"),
                seed = args$seed, max_nodes = args$plot_max_nodes, vertex_size = vsize_neigh)

# Metrics tables (two outputs, same schema)
df_sub <- metrics_table(g_sub, args$node_id_type, id_annot_cache_path)
df_nei <- metrics_table(g_neigh, args$node_id_type, id_annot_cache_path)

write.table(df_sub, file = file.path(args$outdir, "hhnet_subnet_metrics.tsv"),
            sep = "\t", row.names = FALSE, quote = FALSE)
write.table(df_nei, file = file.path(args$outdir, "hhnet_neighbour_metrics.tsv"),
            sep = "\t", row.names = FALSE, quote = FALSE)

message("Done.")
message("Wrote:")
message("  - cluster_map.tsv")
message("  - hhnet_subnet.graphml + hhnet_subnet.png/pdf + hhnet_subnet_metrics.tsv")
message("  - hhnet_subnet_neighbours.graphml + hhnet_subnet_neighbours.png/pdf + hhnet_neighbour_metrics.tsv")
message("  - id_annot_cache.tsv (if biomaRt used)")