fixed10 <- c("tomato","springgreen","royalblue","maroon1","gold","orchid","cyan","yellowgreen","mediumseagreen","saddlebrown")

parse_extra_colours <- function(s) {
  s <- trimws(s)
  if (!nzchar(s)) return(character(0))
  cols <- trimws(strsplit(s, ",", fixed = TRUE)[[1]])
  cols[nzchar(cols)]
}

make_cluster_colours <- function(k, extra_cols = character(0)) {
  if (k <= 10) return(fixed10[seq_len(k)])

  need <- k - 10
  supplied <- extra_cols
  if (length(supplied) < need) {
    gen <- grDevices::hcl.colors(need - length(supplied), palette = "Dark 3")
    supplied <- c(supplied, gen)
  } else {
    supplied <- supplied[seq_len(need)]
  }

  c(fixed10, supplied)
}

